public function reset_dirty($reset_values = false)
{
	$this->p_name_is_dirty = false;
	$this->p_number_is_dirty = false;
	if ($reset_values)
	{
		$this->p_name = $this->p_name_original;
		$this->p_number = $this->p_number_original;
	}
}
public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "insurance_district.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "insurance_district.number":
					$this->set_number($val);
					$this->set_number_original($val);
					break;
				case "insurance_district.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$insurance_district = cls_table_factory::create_instance('insurance_district');
		$row = $db_manager->fetch_row($result);
		$insurance_district->fill($row);
		return $insurance_district;
	}
}

