public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_cache_field.cid":
					$this->set_cid($val);
					$this->set_cid_original($val);
					break;
				case "drupal_cache_field.data":
					$this->set_data($val);
					$this->set_data_original($val);
					break;
				case "drupal_cache_field.expire":
					$this->set_expire($val);
					$this->set_expire_original($val);
					break;
				case "drupal_cache_field.created":
					$this->set_created($val);
					$this->set_created_original($val);
					break;
				case "drupal_cache_field.serialized":
					$this->set_serialized($val);
					$this->set_serialized_original($val);
					break;
				case "drupal_cache_field.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
