public function get_drupal_profiles($db_manager,$application,$search_values,$limit = 0,$offset = 0,$use_key_value = false)
	{
		$result = null;
		if (empty($seach_values))
		{
			$result = $db_manager->search_for_records('drupal_profile',null,null,$limit,$offset);
		}
		else
		{
			$result = $db_manager->search_for_records('drupal_profile',$this->get_search_fields(),$seach_values,$limit,$offset);
