public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "supplier_price.valid_from":
					$this->set_valid_from($val);
					$this->set_valid_from_original($val);
					break;
				case "supplier_price.price":
					$this->set_price($val);
					$this->set_price_original($val);
					break;
				case "supplier_price.qty":
					$this->set_qty($val);
					$this->set_qty_original($val);
					break;
				case "supplier_price.id_supplier_data":
					$this->set_id_supplier_data($val);
					$this->set_id_supplier_data_original($val);
					break;
				case "supplier_price.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
