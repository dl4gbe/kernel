public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "menu_access_group.active":
					$this->set_active($val);
					$this->set_active_original($val);
					break;
				case "menu_access_group.id_access_group":
					$this->set_id_access_group($val);
					$this->set_id_access_group_original($val);
					break;
				case "menu_access_group.id_menu":
					$this->set_id_menu($val);
					$this->set_id_menu_original($val);
					break;
				case "menu_access_group.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
