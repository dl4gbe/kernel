public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_taxonomy_vocabulary.vid":
					$this->set_vid($val);
					$this->set_vid_original($val);
					break;
				case "drupal_taxonomy_vocabulary.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "drupal_taxonomy_vocabulary.machine_name":
					$this->set_machine_name($val);
					$this->set_machine_name_original($val);
					break;
				case "drupal_taxonomy_vocabulary.description":
					$this->set_description($val);
					$this->set_description_original($val);
					break;
				case "drupal_taxonomy_vocabulary.hierarchy":
					$this->set_hierarchy($val);
					$this->set_hierarchy_original($val);
					break;
				case "drupal_taxonomy_vocabulary.module":
					$this->set_module($val);
					$this->set_module_original($val);
					break;
				case "drupal_taxonomy_vocabulary.weight":
					$this->set_weight($val);
					$this->set_weight_original($val);
					break;
				case "drupal_taxonomy_vocabulary.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
