public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_flood.id":
					$this->set_id($val);
					break;
				case "drupal_flood.expiration":
					$this->set_expiration($val);
					$this->set_expiration_original($val);
					break;
				case "drupal_flood.timestamp":
					$this->set_timestamp($val);
					$this->set_timestamp_original($val);
					break;
				case "drupal_flood.identifier":
					$this->set_identifier($val);
					$this->set_identifier_original($val);
					break;
				case "drupal_flood.event":
					$this->set_event($val);
					$this->set_event_original($val);
					break;
				case "drupal_flood.fid":
					$this->set_fid($val);
					$this->set_fid_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
