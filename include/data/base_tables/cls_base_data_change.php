public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "data_change.id_person_user":
					$this->set_id_person_user($val);
					$this->set_id_person_user_original($val);
					break;
				case "data_change.modidate":
					$this->set_modidate($val);
					$this->set_modidate_original($val);
					break;
				case "data_change.data":
					$this->set_data($val);
					$this->set_data_original($val);
					break;
				case "data_change.command":
					$this->set_command($val);
					$this->set_command_original($val);
					break;
				case "data_change.id_data":
					$this->set_id_data($val);
					$this->set_id_data_original($val);
					break;
				case "data_change.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$data_change = cls_table_factory::create_instance('data_change');
		$row = $db_manager->fetch_row($result);
		$data_change->fill($row);
		return $data_change;
	}
