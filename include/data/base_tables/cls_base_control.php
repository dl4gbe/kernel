public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "control.id_ribbonbar":
					$this->set_id_ribbonbar($val);
					$this->set_id_ribbonbar_original($val);
					break;
				case "control.description":
					$this->set_description($val);
					$this->set_description_original($val);
					break;
				case "control.path":
					$this->set_path($val);
					$this->set_path_original($val);
					break;
				case "control.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "control.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$control = cls_table_factory::create_instance('control');
		$row = $db_manager->fetch_row($result);
		$control->fill($row);
		return $control;
	}
