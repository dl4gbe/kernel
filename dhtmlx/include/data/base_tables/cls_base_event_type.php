public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "event_type.id":
					$this->set_id($val);
					break;
				case "event_type.key":
					$this->set_key($val);
					$this->set_key_original($val);
					break;
				case "event_type.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "event_type.tablename":
					$this->set_tablename($val);
					$this->set_tablename_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$event_type = cls_table_factory::create_instance('event_type');
		$row = $db_manager->fetch_row($result);
		$event_type->fill($row);
		return $event_type;
	}
