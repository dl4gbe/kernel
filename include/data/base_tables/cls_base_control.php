		$result = null;
		if (empty($seach_values))
		{
			$result = $db_manager->search_for_records('control',null,null,$limit,$offset);
		}
		else
		{
			$result = $db_manager->search_for_records('control',$this->get_search_fields(),$seach_values,$limit,$offset);
		}
		if (is_null($result)) return null;
		if (count($result) == 0) return null;
