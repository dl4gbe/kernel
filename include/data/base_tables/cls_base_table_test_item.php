public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "table_test_item.source":
					$this->set_source($val);
					$this->set_source_original($val);
					break;
				case "table_test_item.path":
					$this->set_path($val);
					$this->set_path_original($val);
					break;
				case "table_test_item.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "table_test_item.tablename":
					$this->set_tablename($val);
					$this->set_tablename_original($val);
					break;
				case "table_test_item.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
