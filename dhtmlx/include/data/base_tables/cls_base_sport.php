	{
		$this->p_name = $this->p_name_original;
		$this->p_rehasport = $this->p_rehasport_original;
	}
}
public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "sport.id":
					$this->set_id($val);
					break;
				case "sport.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "sport.rehasport":
					$this->set_rehasport($val);
					$this->set_rehasport_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$sport = cls_table_factory::create_instance('sport');
		$row = $db_manager->fetch_row($result);
		$sport->fill($row);
		return $sport;
	}
