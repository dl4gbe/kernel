public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "person_account.id_account_payable":
					$this->set_id_account_payable($val);
					$this->set_id_account_payable_original($val);
					break;
				case "person_account.id_account_receivable":
					$this->set_id_account_receivable($val);
					$this->set_id_account_receivable_original($val);
					break;
				case "person_account.supplieraccountno":
					$this->set_supplieraccountno($val);
					$this->set_supplieraccountno_original($val);
					break;
				case "person_account.customeraccountno":
					$this->set_customeraccountno($val);
					$this->set_customeraccountno_original($val);
					break;
				case "person_account.id_person":
					$this->set_id_person($val);
					$this->set_id_person_original($val);
					break;
				case "person_account.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
