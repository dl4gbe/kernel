public function reset_dirty($reset_values = false)
{
	$this->p_name_is_dirty = false;
	$this->p_type_locale_is_dirty = false;
	$this->p_type_id_is_dirty = false;
	if ($reset_values)
	{
		$this->p_name = $this->p_name_original;
		$this->p_type_locale = $this->p_type_locale_original;
		$this->p_type_id = $this->p_type_id_original;
	}
}
public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "geodb_type_names.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "geodb_type_names.type_locale":
					$this->set_type_locale($val);
					$this->set_type_locale_original($val);
					break;
				case "geodb_type_names.type_id":
					$this->set_type_id($val);
					$this->set_type_id_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$geodb_type_names = cls_table_factory::create_instance('geodb_type_names');
		$row = $db_manager->fetch_row($result);
		$geodb_type_names->fill($row);
		return $geodb_type_names;
	}
