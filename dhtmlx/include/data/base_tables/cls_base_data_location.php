public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "data_location.id":
					$this->set_id($val);
					break;
				case "data_location.id_data":
					$this->set_id_data($val);
					$this->set_id_data_original($val);
					break;
				case "data_location.id_location":
					$this->set_id_location($val);
					$this->set_id_location_original($val);
					break;
				case "data_location.owner":
					$this->set_owner($val);
					$this->set_owner_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
