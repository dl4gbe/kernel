public function get_drupal_search_totals($db_manager,$application,$search_values,$limit = 0,$offset = 0,$use_key_value = false)
	{
		$result = null;
		if (empty($seach_values))
		{
			$result = $db_manager->search_for_records('drupal_search_total',null,null,$limit,$offset);
		}
		else
		{
			$result = $db_manager->search_for_records('drupal_search_total',$this->get_search_fields(),$seach_values,$limit,$offset);
		}
		if (is_null($result)) return null;
		if (count($result) == 0) return null;
		require_once('include/data/table_factory/cls_table_factory.php');
		$data = array();
		while (($row=$db_manager->fetch_by_assoc($result)) !=null)
			{
				$drupal_search_total = cls_table_factory::create_instance('drupal_search_total');
				$drupal_search_total->fill($row);
				$data[] = $drupal_search_total;
			}
		return $data;
	}

