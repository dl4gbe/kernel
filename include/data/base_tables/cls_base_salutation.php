		$this->p_letter = $this->p_letter_original;
		$this->p_formal = $this->p_formal_original;
		$this->p_name = $this->p_name_original;
		$this->p_gender = $this->p_gender_original;
	}
}
public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "salutation.letter":
					$this->set_letter($val);
					$this->set_letter_original($val);
					break;
				case "salutation.formal":
					$this->set_formal($val);
					$this->set_formal_original($val);
					break;
				case "salutation.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "salutation.gender":
					$this->set_gender($val);
					$this->set_gender_original($val);
					break;
				case "salutation.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$salutation = cls_table_factory::create_instance('salutation');
		$row = $db_manager->fetch_row($result);
		$salutation->fill($row);
		return $salutation;
	}
