public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_profile.id":
					$this->set_id($val);
					break;
				case "drupal_profile.changed":
					$this->set_changed($val);
					$this->set_changed_original($val);
					break;
				case "drupal_profile.created":
					$this->set_created($val);
					$this->set_created_original($val);
					break;
				case "drupal_profile.label":
					$this->set_label($val);
					$this->set_label_original($val);
					break;
				case "drupal_profile.uid":
					$this->set_uid($val);
					$this->set_uid_original($val);
					break;
				case "drupal_profile.type":
					$this->set_type($val);
					$this->set_type_original($val);
					break;
				case "drupal_profile.pid":
					$this->set_pid($val);
					$this->set_pid_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
