public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_locales_target.lid":
					$this->set_lid($val);
					$this->set_lid_original($val);
					break;
				case "drupal_locales_target.translation":
					$this->set_translation($val);
					$this->set_translation_original($val);
					break;
				case "drupal_locales_target.language":
					$this->set_language($val);
					$this->set_language_original($val);
					break;
				case "drupal_locales_target.plid":
					$this->set_plid($val);
					$this->set_plid_original($val);
					break;
				case "drupal_locales_target.plural":
					$this->set_plural($val);
					$this->set_plural_original($val);
					break;
				case "drupal_locales_target.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
