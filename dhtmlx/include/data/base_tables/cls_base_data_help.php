public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "data_help.id":
					$this->set_id($val);
					break;
				case "data_help.id_application":
					$this->set_id_application($val);
					$this->set_id_application_original($val);
					break;
				case "data_help.id_data":
					$this->set_id_data($val);
					$this->set_id_data_original($val);
					break;
				case "data_help.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "data_help.content":
					$this->set_content($val);
					$this->set_content_original($val);
					break;
				case "data_help.language":
					$this->set_language($val);
					$this->set_language_original($val);
					break;
				case "data_help.id_person_author":
					$this->set_id_person_author($val);
					$this->set_id_person_author_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
