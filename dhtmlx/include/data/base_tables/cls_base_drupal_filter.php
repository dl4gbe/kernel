public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_filter.format":
					$this->set_format($val);
					$this->set_format_original($val);
					break;
				case "drupal_filter.module":
					$this->set_module($val);
					$this->set_module_original($val);
					break;
				case "drupal_filter.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "drupal_filter.weight":
					$this->set_weight($val);
					$this->set_weight_original($val);
					break;
				case "drupal_filter.status":
					$this->set_status($val);
					$this->set_status_original($val);
					break;
				case "drupal_filter.settings":
					$this->set_settings($val);
					$this->set_settings_original($val);
					break;
				case "drupal_filter.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
