public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "control_group.id":
					$this->set_id($val);
					break;
				case "control_group.id_application":
					$this->set_id_application($val);
					$this->set_id_application_original($val);
					break;
				case "control_group.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "control_group.open_in_own_form":
					$this->set_open_in_own_form($val);
					$this->set_open_in_own_form_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$control_group = cls_table_factory::create_instance('control_group');
		$row = $db_manager->fetch_row($result);
		$control_group->fill($row);
		return $control_group;
	}
