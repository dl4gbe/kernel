public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_queue.id":
					$this->set_id($val);
					break;
				case "drupal_queue.created":
					$this->set_created($val);
					$this->set_created_original($val);
					break;
				case "drupal_queue.expire":
					$this->set_expire($val);
					$this->set_expire_original($val);
					break;
				case "drupal_queue.data":
					$this->set_data($val);
					$this->set_data_original($val);
					break;
				case "drupal_queue.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "drupal_queue.item_id":
					$this->set_item_id($val);
					$this->set_item_id_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
