public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "invoice.id":
					$this->set_id($val);
					break;
				case "invoice.id_person":
					$this->set_id_person($val);
					$this->set_id_person_original($val);
					break;
				case "invoice.invoiceno":
					$this->set_invoiceno($val);
					$this->set_invoiceno_original($val);
					break;
				case "invoice.invoicedate":
					$this->set_invoicedate($val);
					$this->set_invoicedate_original($val);
					break;
				case "invoice.id_person_employee":
					$this->set_id_person_employee($val);
					$this->set_id_person_employee_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
