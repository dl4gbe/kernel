public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_role_permission.id":
					$this->set_id($val);
					break;
				case "drupal_role_permission.module":
					$this->set_module($val);
					$this->set_module_original($val);
					break;
				case "drupal_role_permission.permission":
					$this->set_permission($val);
					$this->set_permission_original($val);
					break;
				case "drupal_role_permission.rid":
					$this->set_rid($val);
					$this->set_rid_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
