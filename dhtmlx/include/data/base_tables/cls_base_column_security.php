public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "column_security.id":
					$this->set_id($val);
					break;
				case "column_security.id_table_columns":
					$this->set_id_table_columns($val);
					$this->set_id_table_columns_original($val);
					break;
				case "column_security.id_access_group":
					$this->set_id_access_group($val);
					$this->set_id_access_group_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
