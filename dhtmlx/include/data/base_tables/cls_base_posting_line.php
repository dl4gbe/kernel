public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "posting_line.id":
					$this->set_id($val);
					break;
				case "posting_line.id_posting_header":
					$this->set_id_posting_header($val);
					$this->set_id_posting_header_original($val);
					break;
				case "posting_line.debit":
					$this->set_debit($val);
					$this->set_debit_original($val);
					break;
				case "posting_line.credit":
					$this->set_credit($val);
					$this->set_credit_original($val);
					break;
				case "posting_line.id_account":
					$this->set_id_account($val);
					$this->set_id_account_original($val);
					break;
				case "posting_line.postingtype":
					$this->set_postingtype($val);
					$this->set_postingtype_original($val);
					break;
				case "posting_line.id_article":
					$this->set_id_article($val);
					$this->set_id_article_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
