	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$invoice = cls_table_factory::create_instance('invoice');
		$row = $db_manager->fetch_row($result);
		$invoice->fill($row);
		return $invoice;
	}
