public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "communication.id":
					$this->set_id($val);
					break;
				case "communication.id_person":
					$this->set_id_person($val);
					$this->set_id_person_original($val);
					break;
				case "communication.value":
					$this->set_value($val);
					$this->set_value_original($val);
					break;
				case "communication.id_communication_type":
					$this->set_id_communication_type($val);
					$this->set_id_communication_type_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$communication = cls_table_factory::create_instance('communication');
		$row = $db_manager->fetch_row($result);
		$communication->fill($row);
		return $communication;
	}
