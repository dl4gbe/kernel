public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "table_column.id":
					$this->set_id($val);
					break;
				case "table_column.table_name":
					$this->set_table_name($val);
					$this->set_table_name_original($val);
					break;
				case "table_column.column_name":
					$this->set_column_name($val);
					$this->set_column_name_original($val);
					break;
				case "table_column.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "table_column.width":
					$this->set_width($val);
					$this->set_width_original($val);
					break;
				case "table_column.default_control":
					$this->set_default_control($val);
					$this->set_default_control_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$table_column = cls_table_factory::create_instance('table_column');
		$row = $db_manager->fetch_row($result);
		$table_column->fill($row);
		return $table_column;
	}
