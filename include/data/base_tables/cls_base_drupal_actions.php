public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_actions.id":
					$this->set_id($val);
					break;
				case "drupal_actions.label":
					$this->set_label($val);
					$this->set_label_original($val);
					break;
				case "drupal_actions.parameters":
					$this->set_parameters($val);
					$this->set_parameters_original($val);
					break;
				case "drupal_actions.callback":
					$this->set_callback($val);
					$this->set_callback_original($val);
					break;
				case "drupal_actions.type":
					$this->set_type($val);
					$this->set_type_original($val);
					break;
				case "drupal_actions.aid":
					$this->set_aid($val);
					$this->set_aid_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$drupal_actions = cls_table_factory::create_instance('drupal_actions');
		$row = $db_manager->fetch_row($result);
		$drupal_actions->fill($row);
		return $drupal_actions;
	}
