public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "communication_type.id":
					$this->set_id($val);
					break;
				case "communication_type.basetype":
					$this->set_basetype($val);
					$this->set_basetype_original($val);
					break;
				case "communication_type.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "communication_type.active":
					$this->set_active($val);
					$this->set_active_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$communication_type = cls_table_factory::create_instance('communication_type');
		$row = $db_manager->fetch_row($result);
		$communication_type->fill($row);
		return $communication_type;
	}
