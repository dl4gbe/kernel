public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_url_alias.id":
					$this->set_id($val);
					break;
				case "drupal_url_alias.language":
					$this->set_language($val);
					$this->set_language_original($val);
					break;
				case "drupal_url_alias.alias":
					$this->set_alias($val);
					$this->set_alias_original($val);
					break;
				case "drupal_url_alias.source":
					$this->set_source($val);
					$this->set_source_original($val);
					break;
				case "drupal_url_alias.pid":
					$this->set_pid($val);
					$this->set_pid_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
