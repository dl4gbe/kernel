public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_block_custom.id":
					$this->set_id($val);
					break;
				case "drupal_block_custom.format":
					$this->set_format($val);
					$this->set_format_original($val);
					break;
				case "drupal_block_custom.info":
					$this->set_info($val);
					$this->set_info_original($val);
					break;
				case "drupal_block_custom.body":
					$this->set_body($val);
					$this->set_body_original($val);
					break;
				case "drupal_block_custom.bid":
					$this->set_bid($val);
					$this->set_bid_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
