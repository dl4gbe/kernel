public function get_drupal_taxonomy_vocabularys($db_manager,$application,$search_values,$limit = 0,$offset = 0,$use_key_value = false)
	{
		$result = null;
		if (empty($seach_values))
		{
			$result = $db_manager->search_for_records('drupal_taxonomy_vocabulary',null,null,$limit,$offset);
		}
		else
		{
			$result = $db_manager->search_for_records('drupal_taxonomy_vocabulary',$this->get_search_fields(),$seach_values,$limit,$offset);
		}
		if (is_null($result)) return null;
		if (count($result) == 0) return null;
		require_once('include/data/table_factory/cls_table_factory.php');
		$data = array();
		while (($row=$db_manager->fetch_by_assoc($result)) !=null)
			{
				$drupal_taxonomy_vocabulary = cls_table_factory::create_instance('drupal_taxonomy_vocabulary');
				$drupal_taxonomy_vocabulary->fill($row);
				$data[] = $drupal_taxonomy_vocabulary;
			}
		return $data;
	}

