public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_rdf_mapping.type":
					$this->set_type($val);
					$this->set_type_original($val);
					break;
				case "drupal_rdf_mapping.bundle":
					$this->set_bundle($val);
					$this->set_bundle_original($val);
					break;
				case "drupal_rdf_mapping.mapping":
					$this->set_mapping($val);
					$this->set_mapping_original($val);
					break;
				case "drupal_rdf_mapping.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
