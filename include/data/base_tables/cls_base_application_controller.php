public function reset_dirty($reset_values = false)
{
	$this->p_source_is_dirty = false;
	$this->p_path_is_dirty = false;
	$this->p_name_is_dirty = false;
	if ($reset_values)
	{
		$this->p_source = $this->p_source_original;
		$this->p_path = $this->p_path_original;
		$this->p_name = $this->p_name_original;
	}
}
public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "application_controller.source":
					$this->set_source($val);
					$this->set_source_original($val);
					break;
				case "application_controller.path":
					$this->set_path($val);
					$this->set_path_original($val);
					break;
				case "application_controller.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "application_controller.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$application_controller = cls_table_factory::create_instance('application_controller');
		$row = $db_manager->fetch_row($result);
		$application_controller->fill($row);
		return $application_controller;
	}
