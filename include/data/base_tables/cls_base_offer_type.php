public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "offer_type.id_article_transport":
					$this->set_id_article_transport($val);
					$this->set_id_article_transport_original($val);
					break;
				case "offer_type.id_article_visit":
					$this->set_id_article_visit($val);
					$this->set_id_article_visit_original($val);
					break;
				case "offer_type.id_article_base":
					$this->set_id_article_base($val);
					$this->set_id_article_base_original($val);
					break;
				case "offer_type.name":
					$this->set_name($val);
					$this->set_name_original($val);
					break;
				case "offer_type.type":
					$this->set_type($val);
					$this->set_type_original($val);
					break;
				case "offer_type.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
