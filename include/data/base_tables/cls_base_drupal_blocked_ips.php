public function reset_dirty($reset_values = false)
{
	$this->p_ip_is_dirty = false;
	$this->p_iid_is_dirty = false;
	if ($reset_values)
	{
		$this->p_ip = $this->p_ip_original;
		$this->p_iid = $this->p_iid_original;
	}
}
public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_blocked_ips.id":
					$this->set_id($val);
					break;
				case "drupal_blocked_ips.ip":
					$this->set_ip($val);
					$this->set_ip_original($val);
					break;
				case "drupal_blocked_ips.iid":
					$this->set_iid($val);
					$this->set_iid_original($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$drupal_blocked_ips = cls_table_factory::create_instance('drupal_blocked_ips');
		$row = $db_manager->fetch_row($result);
		$drupal_blocked_ips->fill($row);
		return $drupal_blocked_ips;
	}
