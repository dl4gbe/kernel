public function reset_dirty($reset_values = false)
{
	$this->p_word_is_dirty = false;
	$this->p_count_is_dirty = false;
	if ($reset_values)
	{
		$this->p_word = $this->p_word_original;
		$this->p_count = $this->p_count_original;
	}
}
public function fill($row,$reset = true)
{
	foreach ($row as $key => $val)
		{
		switch ($key)
			{
				case "drupal_search_total.word":
					$this->set_word($val);
					$this->set_word_original($val);
					break;
				case "drupal_search_total.count":
					$this->set_count($val);
					$this->set_count_original($val);
					break;
				case "drupal_search_total.id":
					$this->set_id($val);
					break;
			}
		}
	if ($reset)
	{
		$this->reset_dirty(false);
	}
}
public function get_by_id($id,$db_manager,$application = null)
{
	$sql = $this->get_table_select($db_manager->get_delimeter());
	$sql .= " where id = '" . $id . "'";
	$result = $db_manager->query($sql);
	if (!is_null($result))
	{
		require_once('include/data/table_factory/cls_table_factory.php');
		$drupal_search_total = cls_table_factory::create_instance('drupal_search_total');
		$row = $db_manager->fetch_row($result);
		$drupal_search_total->fill($row);
		return $drupal_search_total;
	}
}

